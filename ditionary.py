
# a dictionary in python links a key to a value like a word to a definition. Uses { key : definition } format

houses = {"harry": "gryffindor", "draco": "slytherin"}


#Use square brackets to look something up within a dictionary. In this case we look up the key "harry" within the houses dictionary.
print(houses["harry"])

#to add to a dictionary

houses["Hermione"] = "gryffindor"



