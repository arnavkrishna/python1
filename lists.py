
#List of Harry Potter names labelled under variable "names". Lists use [] while tuples use () and cannot be added to
names = ["Harry", "Ron", "Hermione", "Ginny"]
print(names)
print(names[0])


#Append lets us add to the end of a list
names.append("Draco")
print(names[4])

#note that there isnt anything in the parenthesis. names.sort() sorts the list alphabetically.
names.sort()
print(names)